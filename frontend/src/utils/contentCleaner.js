/**
 * contentCleaner.js — חדש
 * מנקה טקסט לימוד (content_md) מ-artifacts של OCR / PDF extraction
 * לשימוש ב-Academy.jsx לפני הצגת חומר עיוני
 *
 * הבעיות שנפתרות (מצילומי מסך):
 *  ✅ "רג ל" → "רגל"  (מילה שנחתכה)
 *  ✅ "דוגמאו ת" → "דוגמאות"
 *  ✅ "עצמ י" → "עצמי"
 *  ✅ "תעבור ה" → "תעבורה"
 *  ✅ "להאט ." → "להאט."  (רווח לפני נקודה בסוף שורה)
 *  ✅ "הגדרות :" → "הגדרות:"
 *  ✅ "ידיה ם" → "ידיהם"  (ם סופית)
 */

// ─── WORD SPLIT FIXER ─────────────────────────────────────────────────────────
// מאחד מילה שנחצתה ע"י OCR: [בסיס][רווח][סיומת]
// ← רק כשהסיומת היא 1-2 אותיות שמאפיינות סיומת מילה עברית

const fixOcrWordSplits = (s) => {
  // אותיות סופיות — לעולם לא מתחילות מילה בעברית
  s = s.replace(/([א-ת]{2,6}) ([ןםףך])(?=[ ,.\n!?:;–\-]|$)/gm, '$1$2');

  // סיומות 2 אותיות: ות ים ית
  s = s.replace(/([א-ת]{2,5}) (ות|ים|ית)(?=[ ,.\n!?:;–\-]|$)/gm, '$1$2');

  // סיומות 1 אות: י ה ת — שכיחות בסוף מילה
  s = s.replace(/([א-ת]{2,5}) ([יהת])(?=[ ,.\n!?:;–\-]|$)/gm, '$1$2');

  // ל בסוף — רק כשהבסיס קצר (2 תווים) כדי לא לגרור שגיאות כמו "ידי ל"
  s = s.replace(/([א-ת]{2,2}) ([ל])(?=[ ,.\n!?:;–\-]|$)/gm, '$1$2');

  return s;
};

// ─── PUNCTUATION CLEANUP ─────────────────────────────────────────────────────

const fixPunctuation = (s) => {
  // רווח לפני פיסוק בסוף שורה: "להאט ." → "להאט."
  s = s.replace(/[ \t]+([.?!,;:])(\s*$)/gm, '$1$2');

  // רווח לפני נקודותיים: "הגדרות :" → "הגדרות:"
  s = s.replace(/[ \t]+:/gm, ':');

  // נקודה בין שתי מילים עבריות ללא רווח: "לנסוע.מהר" → "לנסוע. מהר"
  s = s.replace(/([א-ת])\.([א-ת])/g, '$1. $2');

  // שני רווחים ←
  s = s.replace(/[ \t]{2,}/g, ' ');

  return s;
};

// ─── KNOWN COMPOUND FIXES ────────────────────────────────────────────────────
// מילים ספציפיות מהחוברת שמופיעות שוב ושוב שבורות

const COMPOUND_FIXES = [
  [/\bרמזור הולכי רג ל\b/g, 'רמזור הולכי רגל'],
  [/\bהולכי רג ל\b/g, 'הולכי רגל'],
  [/\bרמז ור\b/g, 'רמזור'],
  [/\bנהיג ה\b/g, 'נהיגה'],
  [/\bנהיג ות\b/g, 'נהיגות'],
  [/\bמהיר ות\b/g, 'מהירות'],
  [/\bתנוע ה\b/g, 'תנועה'],
  [/\bבטיחות ית\b/g, 'בטיחותית'],
  [/\bתעבורת י\b/g, 'תעבורתי'],
  [/\bמכונ ית\b/g, 'מכונית'],
  [/\bרחוב ות\b/g, 'רחובות'],
  [/\bאזהר ה\b/g, 'אזהרה'],
  [/\bסכנ ה\b/g, 'סכנה'],
  [/\bחובר ת\b/g, 'חוברת'],
  [/\bדרישות ית\b/g, 'דרישותית'],
  [/\bפעילות ית\b/g, 'פעילותית'],
];

// ─── MAIN FUNCTION ────────────────────────────────────────────────────────────

/**
 * cleanContentMd — מנקה content_md לפני הצגה
 * @param {string} md — הטקסט/markdown שמגיע מ-Supabase
 * @returns {string} — טקסט מנוקה
 */
export const cleanContentMd = (md) => {
  if (!md) return '';
  let s = String(md);

  // שלב 1: תיקונים מרוכבים ידועים (לפני הכללי)
  COMPOUND_FIXES.forEach(([re, rep]) => { s = s.replace(re, rep); });

  // שלב 2: תיקון מיזוג מילים שנחתכו
  s = fixOcrWordSplits(s);

  // שלב 3: תיקון פיסוק
  s = fixPunctuation(s);

  return s;
};

export default cleanContentMd;
